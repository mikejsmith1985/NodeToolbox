// scripts/generate-automation-mappings.js — Generates docs/automation-mappings.md
// from the report surface registry.
//
// For each report surface NodeToolbox can deliver to an Atlassian Automation
// webhook, this emits the JSON payload shape and a ready-to-paste System Prompt
// template for the Jira "Use Rovo agent" cloud block, referencing the payload via
// {{webhookData.payloadContext.*}} smart values. Driving the docs off the same
// registry the delivery service uses keeps the contract and its documentation in
// lockstep.

'use strict';

const fs = require('fs');
const path = require('path');
const { listSurfaces } = require('../src/services/reportSurfaceRegistry');

/** Builds a representative payloadContext envelope for a surface (for docs). */
function buildEnvelopeExample(surface) {
  return {
    payloadContext: {
      source: surface.id,
      team: { name: 'Team Alpha', projectKey: 'ALPHA' },
      generatedAt: '2026-06-15T11:00:00.000Z',
      report: `<${surface.reportShape}>`,
      meta: { redactionApplied: false, nodeToolboxVersion: '0.16.20' },
    },
  };
}

/** Builds the Rovo "Use Rovo agent" System Prompt template for a surface. */
function buildSystemPrompt(surface) {
  return [
    `You are summarising a NodeToolbox **${surface.label}** for an Agile Release Train.`,
    '',
    'Source: {{webhookData.payloadContext.source}}',
    'Team: {{webhookData.payloadContext.team.name}} ({{webhookData.payloadContext.team.projectKey}})',
    'Generated: {{webhookData.payloadContext.generatedAt}}',
    '',
    'Report content:',
    '{{webhookData.payloadContext.report}}',
    '',
    'Produce a concise summary highlighting the most important changes, risks, and blockers.',
  ].join('\n');
}

/** Builds the full markdown document from a list of surface definitions. */
function generateMarkdown(surfaces) {
  const lines = [
    '# Automation Mappings',
    '',
    '> AUTO-GENERATED by `scripts/generate-automation-mappings.js`. Do not edit by hand.',
    '>',
    '> For each report surface NodeToolbox can deliver to an Atlassian Automation',
    '> webhook, this lists the JSON payload shape and a ready-to-paste System Prompt',
    '> for the Jira "Use Rovo agent" cloud block.',
    '',
    'All payloads share the `payloadContext` envelope; only `report` varies per surface.',
    '',
  ];

  for (const surface of surfaces) {
    lines.push(`## ${surface.label} (\`${surface.id}\`)`);
    lines.push('');
    lines.push(`**Report shape:** ${surface.reportShape}`);
    lines.push('');
    lines.push('**Payload sent:**');
    lines.push('');
    lines.push('```json');
    lines.push(JSON.stringify(buildEnvelopeExample(surface), null, 2));
    lines.push('```');
    lines.push('');
    lines.push('**Rovo "Use Rovo agent" System Prompt template:**');
    lines.push('');
    lines.push('```text');
    lines.push(buildSystemPrompt(surface));
    lines.push('```');
    lines.push('');
  }

  return lines.join('\n');
}

/** Writes the generated markdown to the given path and returns it. */
function writeDocs(outputPath) {
  const markdown = generateMarkdown(listSurfaces());
  fs.writeFileSync(outputPath, markdown, 'utf8');
  return markdown;
}

// When run directly, write docs/automation-mappings.md.
if (require.main === module) {
  const outputPath = path.join(__dirname, '..', 'docs', 'automation-mappings.md');
  writeDocs(outputPath);
  console.log('Wrote ' + outputPath);
}

module.exports = { generateMarkdown, buildSystemPrompt, buildEnvelopeExample, writeDocs };
