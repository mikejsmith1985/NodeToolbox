// scripts/generate-dashboard-module.js — Reads public/toolbox.html and writes
// it as a JavaScript module to src/generated/dashboardHtmlContent.js.
//
// Why this exists:
//   The @yao-pkg/pkg bundler compiles JavaScript modules directly into the exe
//   snapshot, making them always available via require(). However, fs.readFileSync
//   on assets listed in package.json "pkg.assets" is only intercepted when the
//   virtual path matches exactly — and on Windows the snapshot paths are build-
//   machine-specific. Any machine that is NOT the build machine will see ENOENT
//   when readFileSync is called, silently leaving cachedDashboardHtml null and
//   serving the "File Not Found" error page after the setup wizard.
//
//   By converting toolbox.html to a JS module BEFORE running pkg, the HTML
//   content is compiled into the snapshot as code — not as an asset — and
//   require() works correctly on every machine, every time.
//
// When to run:
//   local-release.ps1 runs this automatically before the pkg build step.
//   You can also run it manually: node scripts/generate-dashboard-module.js
//
// Output:
//   src/generated/dashboardHtmlContent.js  (gitignored — build artifact)

'use strict';

const fs   = require('fs');
const path = require('path');

// ── Path Constants ────────────────────────────────────────────────────────────

const PROJECT_ROOT          = path.join(__dirname, '..');
const SOURCE_HTML_PATH      = path.join(PROJECT_ROOT, 'public', 'toolbox.html');
const OUTPUT_DIRECTORY      = path.join(PROJECT_ROOT, 'src', 'generated');
const OUTPUT_MODULE_PATH    = path.join(OUTPUT_DIRECTORY, 'dashboardHtmlContent.js');

// ── Read Source HTML ──────────────────────────────────────────────────────────

const dashboardHtmlContent = fs.readFileSync(SOURCE_HTML_PATH, 'utf-8');

// ── Write JS Module ───────────────────────────────────────────────────────────

// JSON.stringify safely escapes all special characters (quotes, backslashes,
// Unicode) so the string literal is valid JavaScript on any platform.
const generatedModuleSource = [
  '// Auto-generated by scripts/generate-dashboard-module.js — do not edit manually.',
  '// Regenerated before each exe build by local-release.ps1.',
  '// Contains the full toolbox.html content embedded as a JavaScript string so that',
  '// @yao-pkg/pkg compiles it into the snapshot. This guarantees the dashboard HTML',
  '// is available on any machine without relying on fs.readFileSync asset interception.',
  '',
  "'use strict';",
  '',
  'module.exports = ' + JSON.stringify(dashboardHtmlContent) + ';',
  '',
].join('\n');

fs.mkdirSync(OUTPUT_DIRECTORY, { recursive: true });
fs.writeFileSync(OUTPUT_MODULE_PATH, generatedModuleSource, 'utf-8');

console.log(
  '  ✅ Generated ' + path.relative(PROJECT_ROOT, OUTPUT_MODULE_PATH) +
  ' (' + dashboardHtmlContent.length + ' chars)'
);
